const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/user.validation');
const userController = require('../../controllers/user.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageAdmin'), validate(userValidation.createUser), userController.createUser)
  .get(auth('manageAdmin'), validate(userValidation.getUsers), userController.getUsers);

router
  .route('/info/:userId')
  .get(auth('manageAdmin'), validate(userValidation.getUser), userController.getUser)
  .patch(auth('manageAdmin'), validate(userValidation.updateUser), userController.updateUser)
  .delete(auth('manageAdmin'), validate(userValidation.deleteUser), userController.deleteUser);

router
  .route('/change-password/:userId')
  .patch(auth(), validate(userValidation.changePassword), userController.changePassword);

router
  .route('/request/')
  .get(auth('manageAdmin'), userController.getListRequest)
  .post(auth('manageAdmin'), validate(userValidation.acceptRequest), userController.acceptRequest);

module.exports = router;

